#atritubição simples usando apenas o sinal de =
saldo = 500
print(saldo)

#atribuição com valores aritmeticos
saldo += 200 #recebe + 200 no valor final
print(saldo, ' soma')

saldo -= 200 #recebe + 200 no valor final
print(saldo, ' subtração')

saldo *= 5 #recebe duas vezes o seu valor no valor final
print(saldo, ' multiplicação')

saldo //= 2 #recebe apenas o valor inteiro da divisão no valor final
print(saldo, ' inteiro da divisão')

saldo /= 3 #recebe uma divisão por 2 no valor final
print(saldo, ' divisão')

saldo **= 2 #recebe o expoênte nesse caso elevado a 2 no valor final
print(saldo, ' expoênsiação')

saldo %= 3 #modúlo
print(saldo, ' modúlo')
